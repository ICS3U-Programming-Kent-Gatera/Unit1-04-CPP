// This program calculates the area and perimeter of a rectangle.
// With dimensions 9 cm by 3 cmD

#include <iostream>

int main() {
    std::cout << "If a rectangle has the dimensions" << std::endl;
    std::cout << "9 cm by 3 cm" << std::endl;
    std::cout << "" << std::endl;
    std::cout << "Area is: " << (9 * 3) << "cm^2" << std::endl;
    std::cout << "Perimeter is: " << ((9 + 3) * 2) << "cm" << std::endl;
}
