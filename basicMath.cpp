#include <cmath>
#include <iostream>

int main() {
    std::cout << "9+2=" << (9 + 2) << std::endl;
    std::cout << "9-4=" << (9 - 4) << std::endl;
    std::cout << "6/3=" << (6 / 3) << std::endl;
    std::cout << "9+5*2=" << (9 + 5 * 2) << std::endl;
    std::cout << "3+2/2=" << (3 + 2 / 2) << std::endl;
    std::cout << "9/3^2=" << (9 / 3 ^ 2) << std::endl;
    std::cout << "9+pow(2,3)=" << (9 + pow(2, 3)) << std::endl;
}
